package ru.atom.geometry;

public class BarBarCollision extends Exception {

    public BarBarCollision() {
    }

    public BarBarCollision(String message) {
        super(message);
    }

    public BarBarCollision(String message, Throwable cause) {
        super(message, cause);
    }

    public BarBarCollision(Throwable cause) {
        super(cause);
    }

    public BarBarCollision(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
